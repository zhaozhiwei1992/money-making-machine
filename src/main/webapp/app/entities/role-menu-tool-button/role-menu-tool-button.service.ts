import axios from 'axios';

import { IRoleMenuToolButton } from '@/shared/model/role-menu-tool-button.model';

const baseApiUrl = 'api/role-menu-tool-buttons';

export default class RoleMenuToolButtonService {
  public find(id: number): Promise<IRoleMenuToolButton> {
    return new Promise<IRoleMenuToolButton>((resolve, reject) => {
      axios
        .get(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public retrieve(): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .get(baseApiUrl)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public delete(id: number): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .delete(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public create(entity: IRoleMenuToolButton): Promise<IRoleMenuToolButton> {
    return new Promise<IRoleMenuToolButton>((resolve, reject) => {
      axios
        .post(`${baseApiUrl}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public update(entity: IRoleMenuToolButton): Promise<IRoleMenuToolButton> {
    return new Promise<IRoleMenuToolButton>((resolve, reject) => {
      axios
        .put(`${baseApiUrl}/${entity.id}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public partialUpdate(entity: IRoleMenuToolButton): Promise<IRoleMenuToolButton> {
    return new Promise<IRoleMenuToolButton>((resolve, reject) => {
      axios
        .patch(`${baseApiUrl}/${entity.id}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }
}
