import axios from 'axios';

import buildPaginationQueryOpts from '@/shared/sort/sorts';

import { IDataPermissionsRel } from '@/shared/model/data-permissions-rel.model';

const baseApiUrl = 'api/data-permissions-rels';

export default class DataPermissionsRelService {
  public find(id: number): Promise<IDataPermissionsRel> {
    return new Promise<IDataPermissionsRel>((resolve, reject) => {
      axios
        .get(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public retrieve(paginationQuery?: any): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .get(baseApiUrl + `?${buildPaginationQueryOpts(paginationQuery)}`)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public delete(id: number): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .delete(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public create(entity: IDataPermissionsRel): Promise<IDataPermissionsRel> {
    return new Promise<IDataPermissionsRel>((resolve, reject) => {
      axios
        .post(`${baseApiUrl}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public update(entity: IDataPermissionsRel): Promise<IDataPermissionsRel> {
    return new Promise<IDataPermissionsRel>((resolve, reject) => {
      axios
        .put(`${baseApiUrl}/${entity.id}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public partialUpdate(entity: IDataPermissionsRel): Promise<IDataPermissionsRel> {
    return new Promise<IDataPermissionsRel>((resolve, reject) => {
      axios
        .patch(`${baseApiUrl}/${entity.id}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }
}
